package week1day2;

public class PositiveorNegative {

	public static void main(String[] args) {
		int a=12;
		if (a>0)
		{
			System.out.println(a+" is positive number");
		}
		else if (a<0)
		{
			System.out.println(a+ " is negative number");
		}
		else
			System.out.println(a+" is neither positive nor negative");
	}

}
